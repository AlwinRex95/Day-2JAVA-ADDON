package Day2;

public class NumberPatterns {

	public static void main(String[] args) {
		
		int n=5;
		for (int i = 1; i <=n; i++) {
			//i=1; 1<=5 true
			//i++= 1++ 2
			//i=2 2<=5 true
			for (int j = 1; j <=i; j++) {
				
				System.out.print(i);
				
			}
				System.out.println(" ");
		}
		
		
	}

}
